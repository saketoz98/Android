package com.example.android.sknews;

import android.content.Context;
import android.support.annotation.Nullable;
import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

public class SimpleViewAdapterClass extends FragmentPagerAdapter {
    private Context mContext;

    public SimpleViewAdapterClass(Context context,FragmentManager fm) {
        super(fm);
        mContext = context;
    }

    @Override
    public Fragment getItem(int position) {
        if (position == 0) {
            return new TopHeadlines();
        } else {
            return new Technology();
        }
    }

    @Override
    public int getCount() {
        return 2;
    }

    @Nullable
    @Override
    public CharSequence getPageTitle(int position) {
        if (position == 0) {
            return mContext.getString(R.string.category_topHeadlines);
        } else{
            return mContext.getString(R.string.category_technology);
        }
    }
}